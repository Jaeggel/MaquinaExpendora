    public void insertImage(JPanel panel,String ruta,int x, int y)
    {
        ImagePanel Imagen = new ImagePanel(ruta,x,y);
        panel.add(Imagen);
        panel.repaint();
    }
    public class ImagePanel extends JPanel 
    {
        private final String ruta;
        private final int x;
        private final int y;
        public ImagePanel(String ruta,int x,int y)
        {
            //Se crea un mÃ©todo cuyo parÃ¡metro debe ser un objeto Graphics
            this.ruta=ruta;
            this.x=x;
            this.y=y;
            this.setSize(x,y);
        } 
        @Override
        public void paint(Graphics grafico)
        {
            Dimension height = getSize();
            //Se selecciona la imagen que tenemos en el paquete de la //ruta del programa
            ImageIcon Img = new ImageIcon(getClass().getResource(ruta)); 
            //se dibuja la imagen que tenemos en el paquete Images //dentro de un panel
            grafico.drawImage(Img.getImage(), 0, 0, height.width, height.height, null);
            setOpaque(false);
            super.paintComponent(grafico);
        }
    }
